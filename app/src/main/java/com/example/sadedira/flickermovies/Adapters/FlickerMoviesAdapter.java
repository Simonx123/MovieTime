public class FlickerMoviesAdapter extends ArrayAdapter<Movie> {

    public static final int TYPE_ODD = 0;
    public static final int TYPE_EVEN = 1;
    int orientation = getContext().getResources().getConfiguration().orientation;

    // View lookup cache

    private static class ViewHolder {
        TextView title;
        TextView overview;
        ImageView posterImage;

    }
    @Override
    public int getViewTypeCount() {
        return 2;
    }

    @Override
    public int getItemViewType(int position) {
        Movie movie = getItem(position);
        return movie.type();
    }


    public FlickerMoviesAdapter(Context context, List<Movie> movies) {
        super(context, 0, movies);
    }

    @Override
    public View getView(int position, View convertView, ViewGroup parent) {

        // Get the data item for this position
        Movie movie = getItem(position);

        // Check if an existing view is being reused, otherwise inflate the view
        ViewHolder viewHolder; // view lookup cache stored in tag

        if (convertView == null) {
            // If there's no view to re-use, inflate a brand new view for row
            viewHolder = new ViewHolder();
            LayoutInflater inflater = LayoutInflater.from(getContext());
            if (orientation == Configuration.ORIENTATION_PORTRAIT) {
                if (getItemViewType(position) == TYPE_EVEN) {
                    convertView = inflater.inflate(R.layout.list_view_holder, parent, false);
                } else if (getItemViewType(position) == TYPE_ODD) {
                    convertView = inflater.inflate(R.layout.list_view2_holder, parent, false);
                }
            }else{
                convertView = inflater.inflate(R.layout.list_view_holder, parent, false);
            }

            viewHolder.title = (TextView) convertView.findViewById(R.id.mTitle);
            viewHolder.overview = (TextView) convertView.findViewById(R.id.mOverview);
            viewHolder.posterImage = (ImageView) convertView.findViewById(R.id.posterImage);

            viewHolder.posterImage.setImageResource(0);

            // Cache the viewHolder object inside the fresh view
            convertView.setTag(viewHolder);
        } else {

            // View is being recycled, retrieve the viewHolder object from tag
            viewHolder = (ViewHolder) convertView.getTag();
            viewHolder.posterImage.setImageResource(0);
            viewHolder.title.setText("");

           // viewHolder.posterImage.getLayoutParams().width = ViewGroup.LayoutParams.WRAP_CONTENT;

        }



        if (orientation == Configuration.ORIENTATION_PORTRAIT) {
            if (getItemViewType(position) == TYPE_EVEN){
                // into the template view.
                viewHolder.overview.setText("");
                viewHolder.title.setText(movie.getOriginalTitle());
                viewHolder.overview.setText(movie.getOverview());
                Picasso.with(getContext()).load(movie.getPosterPath()).placeholder(R.drawable.anim).transform(new RoundedCornersTransformation(10, 10)).into(viewHolder.posterImage);
            }else{
                //viewHolder.posterImage.getLayoutParams().width = ViewGroup.LayoutParams.MATCH_PARENT;
                viewHolder.title.setText(movie.getOriginalTitle());
                Picasso.with(getContext()).load(movie.getBackDrop()).placeholder(R.drawable.place).transform(new RoundedCornersTransformation(10, 10)).into(viewHolder.posterImage);
            }
        } else if (orientation == Configuration.ORIENTATION_LANDSCAPE) {
            viewHolder.title.setText("");
            viewHolder.overview.setText("");
            Picasso.with(getContext()).load(movie.getBackDrop()).placeholder(R.drawable.place).transform(new RoundedCornersTransformation(10, 10)).into(viewHolder.posterImage);
            viewHolder.title.setText(movie.getOriginalTitle());
            viewHolder.overview.setText(movie.getOverview());
        }

        // Return the completed view to render on screen
        return convertView;


    }
}
